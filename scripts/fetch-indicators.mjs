import fs from "node:fs";
import path from "node:path";
import process from "node:process";
import { pathToFileURL } from "node:url";
import zlib from "node:zlib";

/**
 * Fetch indicator values from official sources and merge them into an edition
 * (matched by indicator id). Used both as a CLI and as a library by
 * publish-edition.mjs.
 *
 * CLI:
 *   node scripts/fetch-indicators.mjs data/drafts/YYYY-MM-DD.json          # dry run
 *   node scripts/fetch-indicators.mjs data/drafts/YYYY-MM-DD.json --write  # update the file
 *
 * Principles:
 *   - Enrich, never destroy. A successful fetch overwrites the value; a failed
 *     fetch preserves manual values; ROBOR is marked stale until BNR recovers.
 *   - Never fabricate. Sources that can't be read simply don't update.
 *   - Only the current day's edition is enriched, so re-publishing an archived
 *     draft never stamps today's live prices onto a past date.
 */

const TIMEOUT_MS = 15000;
const USER_AGENT = "hapics-news-indicators/1.0 (+https://news.hapics.uk)";
const BNR_INTERACTIVE_DB_URL = "https://www.bnr.ro/1074-baza-de-date-interactiva";
const BNR_ROBOR_SERIES = "BBZ_BOR3M";
const BNR_ROBOR_MAX_AGE_DAYS = 7;

// IRCC is quarterly — recalculated once per quarter. Add each quarter's
// official value here (keyed "YYYY-Qn"); ~4 edits per year.
const IRCC_BY_QUARTER = {
  "2026-Q2": 5.58, // valabil 1 apr – 30 iun 2026 (publicat 31 mar 2026)
  "2026-Q3": 5.56, // valabil de la 1 iul 2026
};

async function getText(url, { fetchImpl = fetch } = {}) {
  const controller = new AbortController();
  const timer = setTimeout(() => controller.abort(), TIMEOUT_MS);
  try {
    const res = await fetchImpl(url, { signal: controller.signal, headers: { "user-agent": USER_AGENT } });
    if (!res.ok) throw new Error(`HTTP ${res.status}`);
    return await res.text();
  } finally {
    clearTimeout(timer);
  }
}

export function todayInBucharest(now = new Date()) {
  // en-CA renders YYYY-MM-DD; the explicit timeZone keeps it stable anywhere.
  return new Intl.DateTimeFormat("en-CA", { timeZone: "Europe/Bucharest" }).format(now);
}

function currentQuarter(now = new Date()) {
  const fmt = (opts) => new Intl.DateTimeFormat("en-CA", { timeZone: "Europe/Bucharest", ...opts }).format(now);
  const month = Number(fmt({ month: "numeric" }));
  const year = fmt({ year: "numeric" });
  const q = Math.ceil(month / 3);
  return { year, q, label: `${year}-Q${q}`, start: `${year}-${String((q - 1) * 3 + 1).padStart(2, "0")}-01` };
}

function shiftIsoDate(isoDate, days) {
  const date = new Date(`${isoDate}T12:00:00Z`);
  date.setUTCDate(date.getUTCDate() + days);
  return date.toISOString().slice(0, 10);
}

function toRomanianQueryDate(isoDate) {
  const [year, month, day] = isoDate.split("-");
  return `${day}-${month}-${year}`;
}

function calendarAgeDays(referenceDate, currentDate) {
  const start = Date.parse(`${referenceDate}T00:00:00Z`);
  const end = Date.parse(`${currentDate}T00:00:00Z`);
  return Math.round((end - start) / 86_400_000);
}

function isValidIsoDate(value) {
  const date = new Date(`${value}T00:00:00Z`);
  return !Number.isNaN(date.getTime()) && date.toISOString().slice(0, 10) === value;
}

function bnrRoborExportUrl(currentDate) {
  // BNR's export token is the URL-safe zlib representation generated by the
  // interactive database for a comma-terminated list of series identifiers.
  const indicatorToken = zlib.deflateSync(`${BNR_ROBOR_SERIES},`, { level: 9 }).toString("base64url");
  const url = new URL("https://www.bnr.ro/idbfiles");
  url.searchParams.set("indicatori", indicatorToken);
  url.searchParams.set("dfrom", toRomanianQueryDate(shiftIsoDate(currentDate, -14)));
  url.searchParams.set("dto", toRomanianQueryDate(currentDate));
  url.searchParams.set("format", "CSV");
  return url.toString();
}

/**
 * Parse a number that may use Romanian (1.234,56) or English (1,234.56)
 * grouping. Returns null when the input isn't a finite number.
 */
export function parseLocaleNumber(raw) {
  if (raw == null) return null;
  let s = String(raw).trim().replace(/\s/g, "");
  if (!s) return null;
  const lastComma = s.lastIndexOf(",");
  const lastDot = s.lastIndexOf(".");
  const decimalSep = lastComma > lastDot ? "," : lastDot > lastComma ? "." : "";
  if (decimalSep) {
    const groupSep = decimalSep === "," ? "." : ",";
    s = s.split(groupSep).join("").replace(decimalSep, ".");
  }
  const n = Number(s);
  return Number.isFinite(n) ? n : null;
}

/** Extract a BVB index level from its server-rendered profile page. */
export function parseBvbIndexValue(html) {
  // BVB has no public JSON API; the IndicesProfiles page renders the level
  // server-side. Grab the first index-scale number (>= 100, with decimals)
  // near a value marker. Tolerant of ro/en number formats.
  const candidates = [
    /(?:Valoare|Value|Pret|Last)[^0-9]{0,40}([0-9][0-9.,]{3,})/i,
    /class="[^"]*(?:value|price|last)[^"]*"[^>]*>\s*([0-9][0-9.,]{3,})/i,
  ];
  for (const re of candidates) {
    const n = parseLocaleNumber(html.match(re)?.[1]);
    if (n != null && n >= 100) return n;
  }
  return null;
}

async function fetchEurRon({ fetchImpl = fetch } = {}) {
  const xml = await getText("https://www.bnr.ro/nbrfxrates.xml", { fetchImpl });
  const referenceDate = xml.match(/<Cube date="([^"]+)"/)?.[1];
  const eur = xml.match(/<Rate currency="EUR"[^>]*>([\d.]+)<\/Rate>/)?.[1];
  if (!eur || !referenceDate) throw new Error("EUR rate not found in BNR XML");
  return { value: Number(eur), unit: "lei", referenceDate, freshness: "current",
    note: "Curs de referință oficial publicat de BNR." };
}

function bvbIndexFetcher(symbol, note) {
  return async ({ fetchImpl = fetch, now = new Date() } = {}) => {
    const html = await getText(
      `https://www.bvb.ro/FinancialInstruments/Indices/IndicesProfiles.aspx?i=${symbol}`,
      { fetchImpl },
    );
    const value = parseBvbIndexValue(html);
    if (value == null) throw new Error(`could not parse ${symbol} level from BVB page`);
    return { value, unit: "puncte", referenceDate: todayInBucharest(now), freshness: "current", note };
  };
}

/** Extract the latest ROBOR 3M observation from BNR's semicolon-delimited export. */
export function parseBnrRoborCsv(csv) {
  const text = String(csv).replace(/^\uFEFF/, "");
  if (!new RegExp(`^"";"${BNR_ROBOR_SERIES}"\\r?$`, "m").test(text)) {
    throw new Error(`BNR export does not contain ${BNR_ROBOR_SERIES}`);
  }

  const rows = [...text.matchAll(/^"(\d{4}-\d{2}-\d{2})";"([+-]?[0-9]+(?:[.,][0-9]+)?)"\r?$/gm)]
    .map((match) => ({ referenceDate: match[1], value: parseLocaleNumber(match[2]) }))
    .filter((row) => isValidIsoDate(row.referenceDate) && row.value != null && row.value >= 0 && row.value <= 100)
    .sort((a, b) => b.referenceDate.localeCompare(a.referenceDate));

  if (!rows.length) throw new Error("BNR export contains no valid ROBOR 3M observations");
  return rows[0];
}

export async function fetchRobor({ fetchImpl = fetch, now = new Date() } = {}) {
  const currentDate = todayInBucharest(now);
  const csv = await getText(bnrRoborExportUrl(currentDate), { fetchImpl });
  const latest = parseBnrRoborCsv(csv);
  const ageDays = calendarAgeDays(latest.referenceDate, currentDate);
  if (ageDays < 0) throw new Error(`BNR ROBOR reference date is in the future (${latest.referenceDate})`);
  if (ageDays > BNR_ROBOR_MAX_AGE_DAYS) {
    throw new Error(`BNR ROBOR observation is ${ageDays} days old (${latest.referenceDate})`);
  }
  return { value: latest.value, unit: "%", referenceDate: latest.referenceDate, freshness: "current",
    note: "Rată interbancară de referință, stabilită zilnic de BNR." };
}

async function fetchIrcc({ now = new Date() } = {}) {
  const { label, start, q, year } = currentQuarter(now);
  const value = IRCC_BY_QUARTER[label];
  if (value == null) throw new Error(`IRCC value for ${label} not set in IRCC_BY_QUARTER`);
  const roman = ["I", "II", "III", "IV"][q - 1];
  return { value, unit: "%", referenceDate: start, freshness: "current",
    note: `Indice de referință pentru creditele consumatorilor, valabil în trimestrul ${roman} ${year}.` };
}

// Indicators kept current automatically. Each row is ensured to exist before
// fetching, so the standard set (incl. ROTX and the ROBOR/IRCC split) persists
// across editions even if the upstream draft omits it.
const MANAGED = [
  { id: "eur-ron", label: "EUR/RON", unit: "lei",
    sourceName: "BNR", sourceUrl: "https://www.bnr.ro/nbrfxrates.xml", fetch: fetchEurRon },
  { id: "bet", label: "Indice BET", unit: "puncte",
    sourceName: "BVB", sourceUrl: "https://www.bvb.ro/FinancialInstruments/Indices/IndicesProfiles.aspx?i=BET",
    fetch: bvbIndexFetcher("BET", "Indicele principal al Bursei de Valori București.") },
  { id: "rotx", label: "Indice ROTX", unit: "puncte",
    sourceName: "BVB / Wiener Börse", sourceUrl: "https://www.bvb.ro/FinancialInstruments/Indices/IndicesProfiles.aspx?i=ROTX",
    fetch: bvbIndexFetcher("ROTX", "Indicele acțiunilor românești blue-chip, calculat de Wiener Börse.") },
  { id: "robor", label: "ROBOR 3M", unit: "%",
    sourceName: "BNR", sourceUrl: BNR_INTERACTIVE_DB_URL, fetch: fetchRobor, markStaleOnFailure: true },
  { id: "ircc", label: "IRCC", unit: "%",
    sourceName: "BNR", sourceUrl: "https://www.bnr.ro/", fetch: fetchIrcc },
];

// Legacy indicator ids superseded by managed rows above. Removed during enrich
// so older draft templates (e.g. the combined "ROBOR / IRCC") don't leave a
// stray row beside the new split indicators.
const LEGACY_IDS = ["robor-ircc"];

function ensureIndicator(edition, managed) {
  let indicator = edition.indicators.find((i) => i.id === managed.id);
  if (!indicator) {
    indicator = {
      id: managed.id,
      label: managed.label,
      value: null,
      unit: managed.unit,
      referenceDate: edition.metadata.editionDate,
      sourceName: managed.sourceName,
      sourceUrl: managed.sourceUrl,
      freshness: "unavailable",
    };
    edition.indicators.push(indicator);
  }
  return indicator;
}

/**
 * Enrich an edition's indicators in place. Returns a summary.
 * Only runs for the current day's edition; failures preserve existing values.
 */
export async function enrichIndicators(edition, { log = () => {}, fetchImpl = fetch, now = new Date() } = {}) {
  const summary = { updated: [], kept: [], removed: [], skipped: false };
  if (edition.metadata.editionDate !== todayInBucharest(now)) {
    summary.skipped = true;
    log(`· indicatori: ediție non-curentă (${edition.metadata.editionDate}) — fără preluare automată`);
    return summary;
  }
  for (const id of LEGACY_IDS) {
    const idx = edition.indicators.findIndex((i) => i.id === id);
    if (idx !== -1) {
      edition.indicators.splice(idx, 1);
      summary.removed.push(id);
      log(`− ${id}: eliminat (înlocuit de indicatori separați)`);
    }
  }
  for (const managed of MANAGED) {
    const indicator = ensureIndicator(edition, managed);
    try {
      const result = await managed.fetch({ fetchImpl, now });
      indicator.value = result.value;
      if (result.unit) indicator.unit = result.unit;
      if (result.referenceDate) indicator.referenceDate = result.referenceDate;
      if (result.freshness) indicator.freshness = result.freshness;
      if (result.note) indicator.note = result.note;
      indicator.sourceName = managed.sourceName;
      indicator.sourceUrl = managed.sourceUrl;
      summary.updated.push(managed.id);
      log(`✓ ${managed.id}: ${indicator.value} ${indicator.unit} (${indicator.referenceDate})`);
    } catch (error) {
      if (managed.markStaleOnFailure) {
        indicator.freshness = indicator.value == null ? "unavailable" : "stale";
      }
      summary.kept.push(managed.id);
      log(`· ${managed.id}: ${error.message} → păstrez valoarea existentă (${indicator.value ?? "indisponibil"})`);
    }
  }
  return summary;
}

async function main() {
  const input = process.argv[2];
  const write = process.argv.includes("--write");
  if (!input) {
    console.error("Utilizare: node scripts/fetch-indicators.mjs <edition.json> [--write]");
    process.exit(1);
  }
  const filePath = path.resolve(process.cwd(), input);
  const edition = JSON.parse(fs.readFileSync(filePath, "utf8"));

  await enrichIndicators(edition, { log: (m) => console.log(m) });

  if (write) {
    fs.writeFileSync(filePath, `${JSON.stringify(edition, null, 2)}\n`);
    console.log(`\nActualizat ${path.relative(process.cwd(), filePath)}`);
  } else {
    console.log("\n(dry run — folosește --write pentru a actualiza fișierul)");
  }
  process.exit(0);
}

